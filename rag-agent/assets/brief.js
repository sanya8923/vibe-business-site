  function validateBrief(brief) {
    const missing = [];
    Object.values(BRIEF_FIELDS).forEach(f => {
      if (f.required && (!brief || brief[f.id] === undefined || brief[f.id] === null || brief[f.id] === '')) {
        missing.push(f.id);
      }
    });
    return { ok: missing.length === 0, missing };
  }

  function getBriefValueLabel(fieldId, key) {
    const f = BRIEF_FIELDS[fieldId];
    if (!f) return key || '—';
    const opt = (f.options || []).find(o => o.key === key);
    return opt ? opt.text : (key || '—');
  }

  /** Текст опросника для клиента — кнопка «Скопировать опросник для клиента» */
